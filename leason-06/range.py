"""
loop for with Ranges
"""

# Example 01:

for num in range(11):
    print("-" * 40)
    print(num)

# Example 02:

for num in range(1,11):
    print("*" * 40)
    print(num)

# Example 03:

for num in range(1, 11, 2):
    print("*" * 40)
    print(num)

# Example 04: 

for num in range(10,-1, -1):
    print("*" * 40)
    print(num)
    
